const pangrams = str => {
  let alphabets = [
    "a",
    "b",
    "c",
    "d",
    "e",
    "f",
    "g",
    "h",
    "i",
    "j",
    "k",
    "l",
    "m",
    "n",
    "o",
    "p",
    "q",
    "r",
    "s",
    "t",
    "u",
    "v",
    "w",
    "x",
    "y",
    "z",
  ];

  const res = alphabets.every(al => str.toLowerCase().includes(al));

  return res ? "pangram" : "not pangram";
};

pangrams("The quick brown fox jumps over the lazy dog");
